package kr.ac.skhu.project.traffic;

public class Station {
    private long index;
    private long stationID;
    private String stationName;
    private String x;
    private String y;


    public long getIndex() { return index; }

    public void setIndex(long value) { this.index = value; }


    public long getStationID() { return stationID; }

    public void setStationID(long value) { this.stationID = value; }


    public String getStationName() { return stationName; }

    public void setStationName(String value) { this.stationName = value; }

    public String getX() { return x; }

    public void setX(String value) { this.x = value; }

    public String getY() { return y; }
    public void setY(String value) { this.y = value; }
}